
package model;


public class CadastroMaquina {
    private int id;
    private String nome;
    private int player;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getnome() {
        return nome;
    }

    public void setnome(String nome) {
        this.nome = nome;
    }

    public int getplayer() {
        return player;
    }

    public void setplayer(int player) {
        this.player = player;
    }

   
    
    
    
}
