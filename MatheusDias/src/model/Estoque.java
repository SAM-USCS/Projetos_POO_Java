
package model;


public class Estoque {
    private int id;
    private String peca;
    private int qtd;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getpeca() {
        return peca;
    }

    public void setpeca(String peca) {
        this.peca = peca;
    }

    public int getQtd() {
        return qtd;
    }

    public void setQtd(int qtd) {
        this.qtd = qtd;
    }
    
    
    
}
